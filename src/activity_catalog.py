def get_default_activities() -> Dict[str, Dict[str, Any]]:
    """Return a deep copy of the default activity catalog."""

    return deepcopy(DEFAULT_ACTIVITIES)


def build_activity_summary(activities: Dict[str, Dict[str, Any]]) -> Dict[str, int]:
    """Aggregate a small summary across all activities."""

    total_activities = len(activities)
    total_participants = sum(len(details.get("participants", [])) for details in activities.values())
    total_capacity = sum(int(details.get("max_participants", 0)) for details in activities.values())
    full_activities = sum(
        1
        for details in activities.values()
        if len(details.get("participants", [])) >= int(details.get("max_participants", 0))
    )

    return {
        "activity_count": total_activities,
        "participant_count": total_participants,
        "capacity_total": total_capacity,
        "spots_left": max(total_capacity - total_participants, 0),
        "full_activities": full_activities,
    }